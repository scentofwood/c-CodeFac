#include <stdio.h>
#include <stdlib.h>
#define INF 99999999

/********************************************************************
	큐 
	- PUSH : 스택에 데이터를 넣는다 
	- POP  : 스택에서 데이터를 빼낸다 
	
	Front                                  Rear
	data|next -> data|next -> data|next -> data|next -> NULL
	일반노드     일반노드     일반노드     일반노드
	
********************************************************************/

typedef struct {
	int data;
	struct Node *next;
} Node;

typedef struct {
	Node *front;
	Node *rear;
	int count;
} Queue;

void push(Queue *queue, int data) {
	Node *node = (Node*)malloc(sizeof(Node));
	node->data = data;
	node->next = NULL;
	if (queue->count == 0) {
		queue->front = node;
	}
	else {
		queue->rear->next = node;
	}
	queue->rear = node;
	queue->count++;
}

int pop(Queue *queue) {
	if (queue->count == 0) {
		printf("큐 언더플로우가 발생했습니다.\n");
		return -INF;
	}
	Node *node = queue->front;
	int data = node->data;
	queue->front = node->next;
	free(node);
	queue->count--;
	return data;
}

void show(Queue *queue) {
	Node *cur = queue->front;
	printf("--- 큐의 앞 ---\n");
	while (cur != NULL) {
		printf("%d\n", cur->data);
		cur = cur->next;
	}
	printf("--- 큐의 뒤 ---\n");
}

int main(void) {
	Queue queue;
	queue.front = queue.rear = NULL;
	queue.count = 0;
	push(&queue, 7);
	push(&queue, 5);
	push(&queue, 4);
	show(&queue);
	printf("\n");
	
	pop(&queue);
	push(&queue, 6);
	show(&queue);
	printf("\n");
	
	pop(&queue);
	push(&queue, 1);
	push(&queue, 8);
	pop(&queue);
	show(&queue);
	printf("\n");
	
	system("pause");
	return 0;
}


